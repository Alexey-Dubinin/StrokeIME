
package org.me.strokeime;

public class ColorTheme {
    public final int bg;
    public final int fg;
    public final int fgBright;
    public final int fgDim;

    public ColorTheme(int bg, int fg, int fgBright, int fgDim) {
        this.bg = bg;
        this.fg = fg;
        this.fgBright = fgBright;
        this.fgDim = fgDim;
    }
}
